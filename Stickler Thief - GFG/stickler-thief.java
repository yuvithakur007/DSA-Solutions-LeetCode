class Solution
{
    //Function to find the maximum money the thief can get.
    public int FindMaxSum(int arr[], int n)
    {
        // Your code here
        int s1=0,s2=0;
        for(int i=0;i<n;i++){
            if(i%2==0){
                s1=Math.max(s1+arr[i],s2);
            }
            else{
                s2=Math.max(s2+arr[i],s1);
            }
        }
        if(s1>s2){
            return s1;
        }
        else{
            return s2;
        }
    }
}